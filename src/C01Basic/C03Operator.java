package C01Basic;

public class C03Operator {
    public static void main(String[] args) {
//        a++ : 현재 라인의 명령문이 실행되고 나서 증가
//        ++a : 현재 라인의 명령문이 실행되기 전에 증가
        int a = 10;
        a++;
        System.out.println(a);

        int b = 5;
        int c = b++; // c:5
        int d = ++b; // d:7

//        비트연산자 : 컴퓨터의 2진 체계에서의 연산방식 & | ^ << >>



    }
}
